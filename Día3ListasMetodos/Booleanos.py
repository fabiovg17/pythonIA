mi_boleano = True
print(type(mi_boleano))  # Muestra el tipo de dato (bool)
print(mi_boleano)  # Muestra el valor del booleano (True)

mi_boleano = False
print(type(mi_boleano))  # Muestra el tipo de dato (bool)
print(mi_boleano)  # Muestra el valor del booleano (False)
print(3 > 2)  # Operación mayor que (True)
print(3 < 2)  # Operación menor que (False)
print(3 >= 2)  # Operación mayor o igual que (True)
print(3 <= 2)  # Operación menor o igual que (False)
print(3 == 2)  # Operación igual que (False)
print(3 != 2)  # Operación diferente que (True)
print((3 > 2) and (2 < 3))  # Operación AND (True)
print((3 > 2) and (2 > 3))  # Operación AND (False)
print((3 > 2) or (2 > 3))  # Operación OR (True)
print((3 < 2) or (2 > 3))  # Operación OR (False)
print(not(3 < 2))  # Operación 
print(not(3 > 2))  # Operación NOT (False)

